package cooper;

public class Client {
    private String clientId;
    private String name;
    private String country;

    public Client(String clientId, String name, String country) {
        this.clientId = clientId;
        this.name = name;
        this.clientId = clientId;
    }

    public String getClientId() {
        return clientId;
    }

    public String getName() {
        return name;
    }

    public String getCountry() {
        return country;
    }

}
